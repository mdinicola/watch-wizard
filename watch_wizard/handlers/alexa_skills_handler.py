def handle_api_request(event, context):
    # skip verification if testing locally
    if environ.get('AWS_SAM_LOCAL') == 'true':
        webservice_handler = WebserviceSkillHandler(skill=sb.create(), verify_signature=False, verify_timestamp=False)
    else:
        webservice_handler = WebserviceSkillHandler(skill=sb.create())

    response = webservice_handler.verify_request_and_dispatch(event['headers'], event['body'])
    
    return {
        'statusCode': 200,
        'body': json.dumps(response)
    }

